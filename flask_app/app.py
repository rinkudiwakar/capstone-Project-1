def ensure_nltk_resources() -> None:
    """Ensure resources used in training-time preprocessing are available."""
    resource_checks = {
        "stopwords": lambda: stopwords.words("english"),
        "wordnet": lambda: wordnet.ensure_loaded(),
        "averaged_perceptron_tagger_eng": lambda: pos_tag(["sample"]),
    }

    for resource_name, checker in resource_checks.items():
        try:
            checker()
        except LookupError:
            nltk.download(resource_name, quiet=True)


def get_wordnet_pos(tag: str):
    if tag.startswith("J"):
        return wordnet.ADJ
    if tag.startswith("V"):
        return wordnet.VERB
    if tag.startswith("N"):
        return wordnet.NOUN
    if tag.startswith("R"):
        return wordnet.ADV
    return wordnet.NOUN


def clean_text(text: str, lemmatizer: WordNetLemmatizer, stop_words: set[str]) -> str:
    """Match src/data/data_preprocessing.py behavior for single-text inference."""
    text = str(text)
    text = re.sub(r"<.*?>", " ", text)
    text = re.sub(r"https?://\S+|www\.\S+", " ", text)
    text = text.lower()
    text = re.sub(r"\d+", " ", text)
    text = re.sub(f"[{re.escape(string.punctuation)}]", " ", text)
    text = re.sub(r"[^a-z\s]", " ", text)
    text = re.sub(r"\s+", " ", text).strip()

    if not text:
        return ""

    tokens = [token for token in text.split() if token not in stop_words and len(token) > 1]
    if not tokens:
        return ""

    tagged_tokens = pos_tag(tokens)
    lemmatized_tokens = [
        lemmatizer.lemmatize(token, get_wordnet_pos(tag))
        for token, tag in tagged_tokens
    ]
    return " ".join(lemmatized_tokens)


def preprocess_input_text(text: str) -> str:
    lemmatizer = WordNetLemmatizer()
    stop_words = set(stopwords.words("english"))
    return clean_text(text, lemmatizer, stop_words)


