def get_latest_model_version(model_name: str):
    client = mlflow.MlflowClient()
    for stage in ("Production", "Staging", "None"):
        versions = client.get_latest_versions(model_name, stages=[stage])
        if versions:
            return versions[0].version
    return None


def get_latest_run_id() -> str | None:
    """Read latest run id from reports artifact generated by pipeline."""
    exp_info_path = REPO_ROOT / "reports" / "experiment_info.json"
    if not exp_info_path.exists():
        return None

    try:
        with open(exp_info_path, "r", encoding="utf-8") as f:
            payload = json.load(f)
        return payload.get("run_id")
    except Exception as exc:
        print(f"Warning: failed to parse {exp_info_path}: {exc}")
        return None


def load_pickle_from_mlflow_or_local(file_name: str, run_id: str | None):
    """Load preprocessing artifact from MLflow run first, local models/ as fallback."""
    if run_id:
        artifact_path = f"preprocessing/{file_name}"
        try:
            local_artifact = mlflow.artifacts.download_artifacts(
                run_id=run_id,
                artifact_path=artifact_path,
            )
            with open(local_artifact, "rb") as f:
                print(f"Loaded {file_name} from MLflow artifacts")
                return pickle.load(f)
        except Exception as exc:
            print(f"Warning: could not load {artifact_path} from MLflow run {run_id}: {exc}")

    local_path = REPO_ROOT / "models" / file_name
    if not local_path.exists():
        return None

    with open(local_path, "rb") as f:
        print(f"Loaded {file_name} from local path {local_path}")
        return pickle.load(f)


def load_serving_model():
    """Try MLflow registry first, then fallback to local pickle model."""
    model_name = os.getenv("MLFLOW_MODEL_NAME", "sentiment-classifier")

    try:
        model_version = get_latest_model_version(model_name)
        if model_version is not None:
            model_uri = f"models:/{model_name}/{model_version}"
            print(f"Fetching model from: {model_uri}")
            return mlflow.pyfunc.load_model(model_uri)
    except Exception as exc:
        print(f"Warning: failed to load model from MLflow registry: {exc}")

    local_model_path = REPO_ROOT / "models" / "model.pkl"
    if not local_model_path.exists():
        raise FileNotFoundError(f"Local fallback model not found at {local_model_path}")

    print(f"Using local model fallback: {local_model_path}")
    with open(local_model_path, "rb") as f:
        return pickle.load(f)


def transform_text_to_features(text: str) -> pd.DataFrame:
    """Apply feature engineering sequence used in training."""
    X = vectorizer.transform([text])
    feature_names = vectorizer.get_feature_names_out()

    if variance_selector is not None:
        X = variance_selector.transform(X)
        feature_names = feature_names[variance_selector.get_support()]

    if maxabs_scaler is not None:
        X = maxabs_scaler.transform(X)

    if select_k_best is not None:
        X = select_k_best.transform(X)
        feature_names = feature_names[select_k_best.get_support()]

    feature_values = X.toarray() if hasattr(X, "toarray") else X

    if hasattr(model, "feature_names_in_"):
        expected_columns = list(model.feature_names_in_)
        if len(expected_columns) == feature_values.shape[1]:
            return pd.DataFrame(feature_values, columns=expected_columns)

    return pd.DataFrame(feature_values, columns=[str(col) for col in feature_names])


